using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using DIDCompareMonitor.Repository;
using DIDCompareMonitor.Model;
using System.Data;
using System.Collections;

namespace DIDCompareMonitor.Domain
{
    public class PCB
    {
        private PcbRepository repository = null;

        public PCB(string connectionString, string QSMSConn)
        {
            repository = new PcbRepository(connectionString, QSMSConn);
        }

        public DataTable GetInfo(string Type, string Item, string Side)
        {
            return repository.GetInfo(Type, Item, Side);
        }

        public DataTable ChkData(string Type, string Factory, string Line, string Side, string Machine)
        {
            return repository.ChkData(Type,Factory, Line, Side, Machine);
        }

        public DataTable CompareDID(string CheckType,string OldDID, string ScanDID,string UID)
        {
            return repository.CompareDID(CheckType,OldDID, ScanDID, UID);
        }

    }
}
